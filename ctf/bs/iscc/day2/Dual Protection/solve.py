def ror8(v, n):
    n = n & 7
    return ((v >> n) | (v << (8 - n))) & 0xFF

# Target ciphertext
target = bytes([
    0xC1, 0x8D, 0xA9, 0x81, 0x8F, 0xC4, 0xE0, 0x24,
    0x29, 0x31, 0x71, 0x31, 0x4F, 0x84, 0xC7, 0xC8,
    0x21, 0x88, 0x85, 0x6C, 0x28, 0xCD, 0x7C, 0x44,
    0x72, 0xE1, 0xE5, 0xC0, 0xB0, 0xF4, 0x1C, 0xF8,
    0x12, 0xE4, 0x69, 0x79,
])

key = [0x12, 0x34, 0x56, 0x78, 0x90, 0xAB, 0xCD, 0xEF]

flag = bytearray(36)

for i in range(36):
    b = target[i]

    # Reverse stage3: b = b ^ (i + 0x20)
    b ^= (i + 0x20)

    # Reverse stage2: b = (b ^ key[i % 8]) + 0x7f
    b = (b - 0x7f) & 0xFF
    b ^= key[i % 8]

    # Reverse stage1: b = rol8(b ^ 0x55, 2) + i
    b = (b - i) & 0xFF
    b = ror8(b, 2)
    b ^= 0x55

    flag[i] = b

print("Flag bytes:", bytes(flag))
print("Hex:", bytes(flag).hex())

# Verify by running forward
def rol8(v, n):
    n = n & 7
    return ((v << n) | (v >> (8 - n))) & 0xFF

for i in range(36):
    b = flag[i]
    b = (rol8(b ^ 0x55, 2) + i) & 0xFF
    b = ((b ^ key[i % 8]) + 0x7f) & 0xFF
    b ^= (i + 0x20)
    assert b == target[i], f"Mismatch at {i}"

print("Verification: all bytes match!")
